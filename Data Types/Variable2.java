
package com.mycompany.datatypes;

public class Variable2 {
    
    public static void main(String[] args){
        
        boolean b = false;
        char c = 'b';
        short p = 3525;
        int in = 1020;
        float fl = 12.5f;
        double db = 12.53;
        
        System.out.printf("Boolean b = %b\n",b);
        System.out.printf("Character c = %c\n",c);
        System.out.printf("Short s = %d\n",p);
        System.out.printf("integer in = %d\n",in);
        System.out.printf("Float fl = %.1f\n",fl);
        System.out.printf("Double db = %.2f\n",db);
        
    }
    
}
