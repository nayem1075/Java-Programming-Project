
package com.mycompany.unaryoperator;


public class Program3 {
    
    public static void main(String[] args){
        
        int x = 25;
        int y;
        
        y = ++x;//prefix increement y = 25
        System.out.println("y = "+y);
        y = x;// y = 26
        System.out.println("y = "+y);
        
        
    }
    
}
