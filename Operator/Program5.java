
package com.mycompany.unaryoperator;

public class Program5 {
    
    public static void main(String[] args){
        
        int x = 15;
        int y;
        
        y = --x;//Prefix Decreement y = 14
        System.out.println("y = "+y);
        y = x;//y = 14
        System.out.println("y = "+y);
        
        
    }
    
}
